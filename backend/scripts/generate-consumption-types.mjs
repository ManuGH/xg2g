import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '../..');
const manifestFile = path.join(repoRoot, 'backend/contracts/ui_consumption.manifest.json');
const outputFile = path.join(repoRoot, 'frontend/webui/src/contracts/consumption.d.ts');

const manifest = JSON.parse(fs.readFileSync(manifestFile, 'utf-8'));
const entries = manifest.entries || [];
const normativeFields = new Set(
  entries.filter((entry) => entry.category === 'normative').map((entry) => entry.fieldPath),
);
const telemetryFields = new Set(
  entries.filter((entry) => entry.category === 'telemetry').map((entry) => entry.fieldPath),
);

if (!normativeFields.has('decision.selectedOutputUrl')) {
  throw new Error('ui_consumption.manifest.json is missing normative field decision.selectedOutputUrl');
}
if (!normativeFields.has('decision.mode')) {
  throw new Error('ui_consumption.manifest.json is missing normative field decision.mode');
}
if (!telemetryFields.has('requestId')) {
  throw new Error('ui_consumption.manifest.json is missing telemetry field requestId');
}

const content = `// MECHANICALLY GENERATED by backend/scripts/generate-consumption-types.mjs
// DO NOT EDIT.

export interface NormativeDecision {
    selectedOutputUrl: string;
    selectedOutputKind: string;
    mode: 'direct_play' | 'transcode' | 'deny';
    // Forbidden fields like 'outputs' are NOT here.
}

export interface NormativePlaybackInfo {
    decision: NormativeDecision;
    requestId: string;
    // Legacy URL is NOT here.
}
`;

fs.writeFileSync(outputFile, content);
console.log(`Generated ${path.relative(repoRoot, outputFile)}`);
