#!/usr/bin/env bash
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPO_ROOT="$(cd "${SCRIPT_DIR}/../.." && pwd)"

DOC="${REPO_ROOT}/docs/ops/GENERATED_ARTIFACT_GOVERNANCE.md"

GOVERNED_EXACT=(
  "README.md"
  "deploy/docker-compose.yml"
  "docker-compose.yml"
  "infrastructure/docker/docker-compose.yml"
  "docs/guides/CONFIGURATION.md"
  "docs/guides/CONFIG_SURFACES.md"
  "docs/guides/config.schema.json"
  "docs/ops/DEPLOYMENT_RUNTIME_CONTRACT.md"
  "docs/ops/OPERATIONS_MODEL.md"
  "deploy/xg2g.service"
  "docs/ops/xg2g.service"
  "docs/ops/xg2g-verifier.service"
  "docs/ops/xg2g-verifier.timer"
  "backend/config.generated.example.yaml"
  "backend/internal/api/server_gen.go"
  "backend/internal/control/http/v3/server_gen.go"
  "frontend/webui/src/types/api/consumption.d.ts"
  "openapi/v3.normative.snapshot.yaml"
)

GOVERNED_PREFIXES=(
  "frontend/webui/src/client-ts/"
  "backend/internal/control/http/dist/"
)

fail() {
  echo "ERROR: $*" >&2
  exit 1
}

assert_file() {
  local file="$1"
  [[ -f "${file}" ]] || fail "missing required file: ${file}"
}

assert_contains() {
  local file="$1"
  local needle="$2"
  local label="$3"
  grep -Fq -- "${needle}" "${file}" || fail "${label}: expected '${needle}' in ${file}"
}

relpath() {
  local path="$1"

  if [[ "${path}" == "${REPO_ROOT}/"* ]]; then
    printf '%s\n' "${path#${REPO_ROOT}/}"
    return 0
  fi

  printf '%s\n' "${path}"
}

class_for_path() {
  local rel="$1"
  local file
  local prefix

  for file in "${GOVERNED_EXACT[@]}"; do
    if [[ "${rel}" == "${file}" ]]; then
      printf 'governed\n'
      return 0
    fi
  done

  for prefix in "${GOVERNED_PREFIXES[@]}"; do
    if [[ "${rel}" == "${prefix}"* ]]; then
      printf 'governed\n'
      return 0
    fi
  done

  return 1
}

detect_generated_reason() {
  local file="$1"
  local rel
  rel="$(relpath "${file}")"

  if [[ "${rel}" == backend/internal/control/http/dist/* ]]; then
    printf 'embedded-webui-dist-prefix\n'
    return 0
  fi

  if head -n 5 "${file}" | grep -Eq '^(#|<!--) GENERATED FILE - DO NOT EDIT\. Source:'; then
    printf 'rendered-generated-header\n'
    return 0
  fi

  if head -n 5 "${file}" | grep -Fq '# Canonical deploy bundle file for xg2g systemd installation.'; then
    printf 'deploy-unit-render-marker\n'
    return 0
  fi

  if head -n 5 "${file}" | grep -Fq '# Canonical deploy bundle file for xg2g production compose.'; then
    printf 'deploy-compose-render-marker\n'
    return 0
  fi

  if head -n 5 "${file}" | grep -Fq 'Code generated by github.com/oapi-codegen/oapi-codegen'; then
    printf 'oapi-codegen-marker\n'
    return 0
  fi

  if head -n 1 "${file}" | grep -Fq '// This file is auto-generated by @hey-api/openapi-ts'; then
    printf 'openapi-ts-marker\n'
    return 0
  fi

  if head -n 1 "${file}" | grep -Fq '// MECHANICALLY GENERATED by backend/scripts/generate-consumption-types.mjs'; then
    printf 'consumption-types-marker\n'
    return 0
  fi

  if head -n 1 "${file}" | grep -Fq '# GENERATED FILE - DO NOT EDIT. Source: backend/api/openapi.yaml + backend/contracts/ui_consumption.manifest.json'; then
    printf 'normative-snapshot-marker\n'
    return 0
  fi

  if head -n 5 "${file}" | grep -Fq '# Generated from internal/config/registry.go. Do not edit by hand.'; then
    printf 'config-example-marker\n'
    return 0
  fi

  if head -n 5 "${file}" | grep -Fq 'This file is generated by `cmd/configgen`'; then
    printf 'config-surfaces-marker\n'
    return 0
  fi

  if grep -Eq '^<!-- BEGIN GENERATED CONFIG OPTIONS -->$' "${file}"; then
    printf 'config-generated-block\n'
    return 0
  fi

  return 1
}

assert_governed_generated_file() {
  local file="$1"
  local rel
  local reason

  rel="$(relpath "${file}")"
  reason="$(detect_generated_reason "${file}")" || fail "file does not look like repo-owned generated truth: ${rel}"
  class_for_path "${rel}" >/dev/null || fail "ungoverned generated truth: ${rel} (${reason})"
}

verify_doc_contract() {
  assert_file "${DOC}"

  assert_contains "${DOC}" 'Config Surfaces' "governance doc class"
  assert_contains "${DOC}" 'Rendered Docs and Units' "governance doc class"
  assert_contains "${DOC}" 'OpenAPI Go Codegen' "governance doc class"
  assert_contains "${DOC}" 'OpenAPI Hard Mode Artifacts' "governance doc class"
  assert_contains "${DOC}" 'Embedded WebUI Dist' "governance doc class"
  assert_contains "${DOC}" 'Local-only Generated Outputs' "governance doc local-only section"
  assert_contains "${DOC}" 'make generate-config' "governance doc generator"
  assert_contains "${DOC}" 'make docs-render' "governance doc generator"
  assert_contains "${DOC}" 'make generate' "governance doc generator"
  assert_contains "${DOC}" 'make gen-openapi-hard' "governance doc generator"
  assert_contains "${DOC}" 'make ui-build' "governance doc generator"
  assert_contains "${DOC}" 'committed required' "governance doc commit policy"
  assert_contains "${DOC}" 'never committed' "governance doc local-only policy"
  assert_contains "${DOC}" 'ungoverned generated truth' "governance doc ungoverned policy"
  assert_contains "${DOC}" 'backend/internal/control/http/dist/' "governance doc dist prefix"
  assert_contains "${DOC}" 'frontend/webui/src/client-ts/' "governance doc client prefix"
  assert_contains "${DOC}" 'backend/scripts/verify-generated-artifacts-contract.sh' "governance doc verifier path"
}

verify_governed_paths_exist() {
  local rel
  local prefix
  local count

  for rel in "${GOVERNED_EXACT[@]}"; do
    assert_file "${REPO_ROOT}/${rel}"
  done

  for prefix in "${GOVERNED_PREFIXES[@]}"; do
    count="$(git ls-files "${prefix}" | wc -l | tr -d '[:space:]')"
    [[ "${count}" != "0" ]] || fail "governed prefix has no tracked files: ${prefix}"
  done
}

verify_marker_coverage() {
  local rel
  local reason

  while IFS= read -r rel; do
    [[ -f "${REPO_ROOT}/${rel}" ]] || continue

    case "${rel}" in
      backend/vendor/*|frontend/webui/dist/*|node_modules/*)
        continue
        ;;
    esac

    reason="$(detect_generated_reason "${REPO_ROOT}/${rel}")" || continue
    class_for_path "${rel}" >/dev/null || fail "ungoverned generated truth: ${rel} (${reason})"
  done < <(git ls-files)
}

verify_local_only_outputs_not_tracked() {
  local tracked

  tracked="$(git ls-files 'frontend/webui/dist' 'artifacts' 'coverage.out' 'coverage.html')"
  if [[ -n "${tracked}" ]]; then
    echo "Tracked local-only generated outputs:" >&2
    printf '%s\n' "${tracked}" >&2
    fail "local-only generated outputs must never be tracked"
  fi
}

verify_negative_guard() {
  local tmpdir
  local rogue_file

  tmpdir="${REPO_ROOT}/.tmp-generated-governance"
  rogue_file="${tmpdir}/rogue.gen.ts"
  rm -rf "${tmpdir}"
  mkdir -p "${tmpdir}"

  cat <<'EOF' > "${rogue_file}"
// This file is auto-generated by @hey-api/openapi-ts
export const rogue = true;
EOF

  if "${BASH_SOURCE[0]}" --assert-generated-file "${rogue_file}" >/dev/null 2>&1; then
    rm -rf "${tmpdir}"
    fail "negative guard failed: rogue generated file unexpectedly passed governance"
  fi

  rm -rf "${tmpdir}"
}

main() {
  case "${1:-}" in
    --assert-generated-file)
      [[ "$#" -eq 2 ]] || fail "usage: $0 --assert-generated-file <file>"
      assert_governed_generated_file "$2"
      echo "OK: governed generated file."
      return 0
      ;;
    "")
      ;;
    *)
      fail "usage: $0 [--assert-generated-file <file>]"
      ;;
  esac

  verify_doc_contract
  verify_governed_paths_exist
  verify_marker_coverage
  verify_local_only_outputs_not_tracked
  verify_negative_guard

  echo "OK: generated artifact governance contract holds."
}

main "$@"
